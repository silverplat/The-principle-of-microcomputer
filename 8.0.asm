;使六位数码管从右到左逐位显示移动的数字0-9
DATA SEGMENT
TABLE1:
	DB 3FH
	DB 06H
	DB 5BH
	DB 4FH
	DB 66H
	DB 6DH
	DB 7DH
	DB 07H
	DB 7FH
	DB 6FH
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,TABLE1
	MOV DX,0606H
	MOV AL,81H
	OUT DX,AL
	MOV DX,0602H
	MOV AL,3FH
	OUT DX,AL
	MOV DX,0600H
	MOV AL,00H
	OUT DX,AL
	MOV CX,0AH
	MOV BX,0000H
X1:
	PUSH CX
	MOV CX,06H
	MOV AL,11011111B
X2:
	MOV DX,0600H
	OUT DX,AL
	ROR AL,1
	
	PUSH AX
	MOV AL,[BX+SI]
	MOV DX,0602H
	OUT DX,AL
	POP AX
	
	CALL DELAY
	LOOP X2
	POP CX
	INC BX
	LOOP X1
	JMP START
	
DELAY:
	PUSH CX
	MOV CX,0FFFFH
	LOOP $
	MOV CX,0FFFFH
	LOOP $
	POP CX
	RET
CODE ENDS
	END START